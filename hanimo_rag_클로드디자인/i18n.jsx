const I18nCtx = React.createContext({ lang: 'en', t: (k) => k, setLang: () => {} });
const useI18n = () => React.useContext(I18nCtx);

function I18nProvider({ children }) {
  const [lang, setLangState] = React.useState(() => localStorage.getItem('hanimo-lang') || 'en');
  const setLang = (l) => { setLangState(l); localStorage.setItem('hanimo-lang', l); };
  const t = (key, ...args) => {
    const v = STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
    return typeof v === 'function' ? v(...args) : v;
  };
  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

window.I18nProvider = I18nProvider;
window.useI18n = useI18n;
